
public class E26_Array2D {

	public static void main(String[] args) {
		//정수형 배열 3행 4열짜리 생성
		//정수형 길이가 4인 배열을 3개 생성
		int[][] arr = new int[3][4];
		
		System.out.println("행 개수 : "+arr.length);
		System.out.println("0번 행의 개수 : " + arr[0].length);
		System.out.println("1번 행의 개수 : " + arr[1].length);
		System.out.println("2번 행의 개수 : " + arr[2].length);
	}

}
