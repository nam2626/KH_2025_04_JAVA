import java.util.Scanner; // 사용자로부터 입력을 받기 위해 Scanner 클래스를 가져옵니다.

// 사용자로부터 숫자를 입력받아 해당 숫자만큼의 높이를 가지는 직각삼각형 모양의 별(*)을 출력하는 클래스
public class E04_Quest {
	/*
	 * 숫자 입력 > 5		숫자 입력 > 7  <-- 사용 예시: 입력값에 따라 출력되는 별(*)의 높이가 달라짐
	 * * *				*
	 * **				**
	 * ***				***
	 * ****			****
	 * *****			*****
	 * ******
	 * *******
	 * * * */
	// 프로그램 실행 시작점 (main 메소드)
	public static void main(String[] args) {
		// 키보드 입력을 받기 위한 Scanner 객체 생성
		Scanner sc = new Scanner(System.in);
		
		// 사용자에게 숫자 입력을 요청하는 메시지 출력
		System.out.print("숫자 입력 : ");
		// 사용자가 입력한 정수를 읽어와서 ea 변수에 저장 (이 변수는 출력할 삼각형의 높이가 됩니다)
		int ea = sc.nextInt();
		
		// 바깥쪽 for문: 삼각형의 행(세로줄)을 제어합니다. 
		// i는 0부터 사용자가 입력한 숫자(ea)보다 작을 때까지 1씩 증가합니다. (총 ea번 반복)
		for(int i = 0; i < ea; i++) { 
			// 안쪽 for문: 각 행에 출력할 별(*)의 개수(가로줄)를 제어합니다.
			// j는 0부터 현재 행 번호(i)까지 1씩 증가합니다. (총 i+1번 반복)
			// 예: i가 0일 때 1번, i가 1일 때 2번, ..., i가 ea-1일 때 ea번 반복합니다.
			for(int j = 0; j < i + 1; j++) { 
				// 별(*) 문자를 출력합니다. print 메소드는 줄바꿈을 하지 않습니다.
				System.out.print("*"); 
			}
			// 안쪽 루프가 끝나면 (한 행에 필요한 만큼의 별 출력이 완료되면) 
			// println 메소드를 호출하여 다음 줄로 커서를 이동시킵니다.
			System.out.println(); 
		}
		
		// Scanner 사용이 끝났으므로 닫아주는 것이 좋습니다 (자원 누수 방지)
		sc.close(); 
	}

}