// 구구단을 가로 형태로 출력하는 예제 클래스
public class E03_Quest {

	// 프로그램 실행 시작점 (main 메소드)
	public static void main(String[] args) {

		// 바깥쪽 for문: 곱해지는 수 (i)를 1부터 9까지 증가시킵니다.
		// 구구단의 각 행을 제어합니다 (예: 2*1, 3*1, ..., 9*1 행)
		for (int i = 1; i < 10; i++) {

			// 안쪽 for문: 단 (dan)을 2단부터 9단까지 증가시킵니다.
			// 구구단의 각 열을 제어합니다 (예: 2단, 3단, ..., 9단)
			for (int dan = 2; dan < 10; dan++) {
				// printf를 사용하여 형식에 맞춰 구구단 한 줄을 출력합니다.
				// %d: 정수 (decimal)
				// %2d: 정수를 2자리 공간을 확보하여 출력 (오른쪽 정렬)
				System.out.printf("%d * %d = %2d", dan, i, dan * i);

				// 현재 출력하는 단(dan)이 9단이 아니라면 (즉, 마지막 단이 아니라면)
				// 각 단의 출력 결과 사이에 구분자 " | "를 추가합니다.
				if (dan < 9)
					System.out.print(" | ");
			}
			// 안쪽 루프가 한 번 완전히 실행되면 (즉, 2단부터 9단까지의 i 곱셈이 모두 출력되면)
			// 다음 줄로 넘어가기 위해 줄바꿈 문자를 출력합니다.
			System.out.println();
		}
	}

}