
public class E03_VariableTest {
	/*
	 * 변수(Variable) : 데이터 하나를 저장할 공간
	 * 
	 */
	public static void main(String[] args) {
		//변수 선언 부분 --> 정수하나 저장할 공간을 만들겠다.
		int num;
		//변수값 초기화(initialize) --> 해당 변수에 값을 저장
		num = 100;
		num = 50; // 기존값을 없어지고 새 값이 저장
		//num에 저장된 값을 읽어온 후에 출력
		System.out.println(num);
		
		int age = 20; //변수를 선언 후에 바로 초기화까지 하겠다.
		System.out.println(age);
		
	}

}
